import 'server-only'
import { openai }             from '@/lib/openai'
import type { StateJson }     from '@/lib/persistence/reflections'

/**
 * framework-analysis.ts — CANON-AWARE REFLECTION ANALYSIS ENGINE
 *
 * Analyzes a user's reflection submission using four canonical frameworks:
 *   1. Life Chapters (12 terrains) — where are they?
 *   2. Meaning Levels (7 levels) — why does this matter to them?
 *   3. Flow Conditions (4 conditions) — how are they moving?
 *   4. Calling Aims (4 aims) — toward what?
 *
 * Also maintains backward compatibility by outputting:
 *   - Five-dimension state assessment (intention/volition/cognition/emotion/action)
 *   - Guidance (what's happening, what's being asked, next steps, reflection prompt)
 *
 * The old Rite/Stage system is NO LONGER generated by the AI.
 * Old DB columns (rite, stage) will receive mapped values for backward compat.
 */

// ── Input ────────────────────────────────────────────────────────────────────

export type FrameworkInput = {
  dimensionScores: {
    intention: number   // aether score 0–100
    volition:  number   // fire
    cognition: number   // air
    emotion:   number   // water
    action:    number   // earth
  }

  narrative: {
    life_phase?:        string | null
    challenges?:        string | null
    direction?:         string | null
    environment?:       string | null
    recurring_pattern?: string | null
    avoidance?:         string | null
    deeper_pull?:       string | null
    energy_state?:      string | null
    energy_sources?:    string | null
  }

  newReflection:      string
  recentReflections:  string[]
  archetypeLabel?:    string | null
  growthEdge?:        string | null
  shadowTrigger?:     string | null
}

// ── Output ───────────────────────────────────────────────────────────────────

export type FrameworkOutput = {
  // Canon-aligned layers
  life_chapter: {
    name:        string
    confidence:  'low' | 'medium' | 'high'
    explanation: string
  }
  meaning_level: {
    name:        string
    confidence:  'low' | 'medium' | 'high'
    explanation: string
  }
  flow_snapshot: {
    activation:    number  // 1–10
    alignment:     number
    attunement:    number
    attentiveness: number
  }
  calling_snapshot: {
    connection:   number   // 1–10
    contribution: number
    creativity:   number
    capability:   number
  }

  // Five-dimension state assessment (retained)
  state: StateJson

  // Guidance
  guidance: {
    what_is_happening:   string
    what_is_being_asked: string
    next_steps:          string[]
    reflection_prompt:   string
  }

  reasoning_summary: string
}

// ── Helpers ──────────────────────────────────────────────────────────────────

function scoreToLevel(score: number): 'low' | 'moderate' | 'high' {
  if (score <= 40) return 'low'
  if (score <= 65) return 'moderate'
  return 'high'
}

// ── JSON Schema ──────────────────────────────────────────────────────────────

